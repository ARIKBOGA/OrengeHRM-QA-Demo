async function extractDbSchema(): Promise<void> {
  console.info('\n📦 Extracting DB schema...');

  const dbName = config.db.name;
  if (!dbName) throw new Error('Database name is required in config');

  const conn = await mysql.createConnection({
    host: config.db.host,
    port: config.db.port,
    database: dbName,
    user: config.db.user,
    password: config.db.password,
  });

  try {
    // 1 — Tüm tablolar
    const [tables] = await conn.execute<any[]>(
      `
      SELECT TABLE_NAME, TABLE_ROWS, TABLE_COMMENT
      FROM INFORMATION_SCHEMA.TABLES
      WHERE TABLE_SCHEMA = ?
      AND TABLE_TYPE = 'BASE TABLE'
      ORDER BY TABLE_NAME
    `,
      [dbName]
    );

    // 2 — Tüm kolonlar
    const [columns] = await conn.execute<any[]>(
      `
      SELECT 
        TABLE_NAME,
        COLUMN_NAME,
        ORDINAL_POSITION,
        COLUMN_DEFAULT,
        IS_NULLABLE,
        DATA_TYPE,
        CHARACTER_MAXIMUM_LENGTH,
        NUMERIC_PRECISION,
        NUMERIC_SCALE,
        COLUMN_TYPE,
        COLUMN_KEY,
        EXTRA,
        COLUMN_COMMENT
      FROM INFORMATION_SCHEMA.COLUMNS
      WHERE TABLE_SCHEMA = ?
      ORDER BY TABLE_NAME, ORDINAL_POSITION
    `,
      [dbName]
    );

    // 3 — Foreign keys
    const [foreignKeys] = await conn.execute<any[]>(
      `
      SELECT
        kcu.TABLE_NAME,
        kcu.COLUMN_NAME,
        kcu.REFERENCED_TABLE_NAME,
        kcu.REFERENCED_COLUMN_NAME,
        rc.UPDATE_RULE,
        rc.DELETE_RULE
      FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE kcu
      JOIN INFORMATION_SCHEMA.REFERENTIAL_CONSTRAINTS rc
        ON rc.CONSTRAINT_NAME = kcu.CONSTRAINT_NAME
        AND rc.CONSTRAINT_SCHEMA = kcu.TABLE_SCHEMA
      WHERE kcu.TABLE_SCHEMA = ?
      AND kcu.REFERENCED_TABLE_NAME IS NOT NULL
      ORDER BY kcu.TABLE_NAME, kcu.COLUMN_NAME
    `,
      [dbName]
    );

    // 4 — Index'ler
    const [indexes] = await conn.execute<any[]>(
      `
      SELECT
        TABLE_NAME,
        INDEX_NAME,
        NON_UNIQUE,
        COLUMN_NAME,
        SEQ_IN_INDEX
      FROM INFORMATION_SCHEMA.STATISTICS
      WHERE TABLE_SCHEMA = ?
      ORDER BY TABLE_NAME, INDEX_NAME, SEQ_IN_INDEX
    `,
      [dbName]
    );

    // ── Markdown üret ────────────────────────────────────────────────────────

    let md = header('OrangeHRM Database Schema');

    // Table of contents
    md += '## Tables\n\n';
    for (const table of tables) {
      md += `- [${table.TABLE_NAME}](#${table.TABLE_NAME.toLowerCase().replace(/_/g, '-')})\n`;
    }
    md += '\n---\n\n';

    // Group columns by table
    const columnsByTable = new Map<string, any[]>();
    for (const col of columns) {
      if (!columnsByTable.has(col.TABLE_NAME)) columnsByTable.set(col.TABLE_NAME, []);
      columnsByTable.get(col.TABLE_NAME)!.push(col);
    }

    // Group FK by table
    const fkByTable = new Map<string, any[]>();
    for (const fk of foreignKeys) {
      if (!fkByTable.has(fk.TABLE_NAME)) fkByTable.set(fk.TABLE_NAME, []);
      fkByTable.get(fk.TABLE_NAME)!.push(fk);
    }

    // Group indexes by table
    const idxByTable = new Map<string, any[]>();
    for (const idx of indexes) {
      if (!idxByTable.has(idx.TABLE_NAME)) idxByTable.set(idx.TABLE_NAME, []);
      idxByTable.get(idx.TABLE_NAME)!.push(idx);
    }

    // Per-table detail
    for (const table of tables) {
      const tableName = table.TABLE_NAME;
      md += `## ${tableName}\n\n`;

      // Columns
      md += '### Columns\n\n';
      md += '| Column | Type | Nullable | Default | Key | Extra |\n';
      md += '|--------|------|----------|---------|-----|-------|\n';

      const cols = columnsByTable.get(tableName) ?? [];
      for (const col of cols) {
        const nullable = col.IS_NULLABLE === 'YES' ? '✅' : '❌';
        const colDefault = col.COLUMN_DEFAULT ?? '';
        const key = col.COLUMN_KEY || '';
        const extra = col.EXTRA || '';
        md += `| \`${col.COLUMN_NAME}\` | \`${col.COLUMN_TYPE}\` | ${nullable} | ${colDefault} | ${key} | ${extra} |\n`;
      }
      md += '\n';

      // Foreign keys
      const fks = fkByTable.get(tableName);
      if (fks && fks.length > 0) {
        md += '### Foreign Keys\n\n';
        md += '| Column | References | On Update | On Delete |\n';
        md += '|--------|------------|-----------|----------|\n';
        for (const fk of fks) {
          md += `| \`${fk.COLUMN_NAME}\` | \`${fk.REFERENCED_TABLE_NAME}.${fk.REFERENCED_COLUMN_NAME}\` | ${fk.UPDATE_RULE} | ${fk.DELETE_RULE} |\n`;
        }
        md += '\n';
      }

      // Indexes
      const idxs = idxByTable.get(tableName);
      if (idxs && idxs.length > 0) {
        // Group by index name
        const indexGroups = new Map<string, any[]>();
        for (const idx of idxs) {
          if (!indexGroups.has(idx.INDEX_NAME)) indexGroups.set(idx.INDEX_NAME, []);
          indexGroups.get(idx.INDEX_NAME)!.push(idx);
        }

        md += '### Indexes\n\n';
        md += '| Index | Columns | Unique |\n';
        md += '|-------|---------|--------|\n';
        for (const [idxName, idxCols] of indexGroups) {
          const cols = idxCols
            .sort((a, b) => a.SEQ_IN_INDEX - b.SEQ_IN_INDEX)
            .map((i) => `\`${i.COLUMN_NAME}\``)
            .join(', ');
          const unique = idxCols[0].NON_UNIQUE === 0 ? '✅' : '❌';
          md += `| \`${idxName}\` | ${cols} | ${unique} |\n`;
        }
        md += '\n';
      }

      md += '---\n\n';
    }

    writeContext('db-schema.md', md);
  } finally {
    await conn.end();
  }
}

