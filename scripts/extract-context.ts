import { execSync } from 'child_process';

async function extractApiContracts(): Promise<void> {
  console.info('\n📡 Extracting API contracts from PHP source...');

  // 1 — Container'daki tüm API PHP dosyalarını bul
  const findCmd = `docker exec orangehrm find /var/www/html/src -name "*API.php" -not -path "*/vendor/*" 2>/dev/null`;
  const fileList = execSync(findCmd).toString().trim().split('\n').filter(Boolean);

  console.info(`   Found ${fileList.length} API files`);

  let md = header('OrangeHRM API Contracts');
  md += '## Overview\n\n';
  md += '> Extracted from PHP OpenAPI annotations in the OrangeHRM source code.\n\n';
  md += '### Base URL\n\n';
  md += '```\nhttp://localhost:8080/web/index.php/api/v2\n```\n\n';
  md += '### Authentication\n\n';
  md += '> Cookie-based session auth. Login via UI, inject `_orangehrm` cookie into API requests.\n\n';
  md += '---\n\n';

  // 2 — Her dosyayı parse et
  const endpoints: ParsedEndpoint[] = [];

  for (const filePath of fileList) {
    try {
      const content = execSync(`docker exec orangehrm cat "${filePath}" 2>/dev/null`).toString();
      const parsed = parseApiFile(filePath, content);
      endpoints.push(...parsed);
    } catch {
      // dosya okunamazsa atla
    }
  }

  // 3 — Plugin'e göre grupla
  const byPlugin = new Map<string, ParsedEndpoint[]>();
  for (const ep of endpoints) {
    if (!byPlugin.has(ep.plugin)) byPlugin.set(ep.plugin, []);
    byPlugin.get(ep.plugin)!.push(ep);
  }

  // 4 — Markdown üret
  for (const [plugin, eps] of [...byPlugin.entries()].sort()) {
    md += `## ${plugin}\n\n`;

    for (const ep of eps) {
      md += `### ${ep.method} ${ep.path}\n\n`;
      if (ep.summary) md += `**Summary:** ${ep.summary}\n\n`;

      if (ep.requestFields.length > 0) {
        md += '**Request Body:**\n\n';
        md += '| Field | Type | Required | Max Length | Notes |\n';
        md += '|-------|------|----------|------------|-------|\n';
        for (const f of ep.requestFields) {
          md += `| \`${f.name}\` | \`${f.type}\` | ${f.required ? '✅' : '❌'} | ${f.maxLength ?? '-'} | ${f.notes ?? ''} |\n`;
        }
        md += '\n';
      }

      if (ep.responseFields.length > 0) {
        md += '**Response Fields:**\n\n';
        md += '| Field | Type | Notes |\n';
        md += '|-------|------|-------|\n';
        for (const f of ep.responseFields) {
          md += `| \`${f.name}\` | \`${f.type}\` | ${f.notes ?? ''} |\n`;
        }
        md += '\n';
      }

      if (ep.paramRules.length > 0) {
        md += '**Validation Rules:**\n\n';
        for (const rule of ep.paramRules) {
          md += `- \`${rule}\`\n`;
        }
        md += '\n';
      }

      md += '---\n\n';
    }
  }

  writeContext('api-contracts.md', md);
  console.info(`   Parsed ${endpoints.length} endpoints`);
}

// ─────────────────────────────────────────────────────────────────────────────
// PARSER
// ─────────────────────────────────────────────────────────────────────────────

interface ParsedField {
  name: string;
  type: string;
  required?: boolean;
  maxLength?: number;
  notes?: string;
}

interface ParsedEndpoint {
  plugin: string;
  method: string;
  path: string;
  summary: string;
  requestFields: ParsedField[];
  responseFields: ParsedField[];
  paramRules: string[];
}

function parseApiFile(filePath: string, content: string): ParsedEndpoint[] {
  const endpoints: ParsedEndpoint[] = [];

  // Plugin adını path'den çıkar
  const pluginMatch = filePath.match(/plugins\/(\w+)\/Api\//);
  const plugin = pluginMatch ? pluginMatch[1].replace('orangehrm', '').replace('Plugin', '').trim() || 'Core' : 'Unknown';

  // @OA\Get, @OA\Post, @OA\Put, @OA\Delete bloklarını bul
  const methodPattern = /@OA\\(Get|Post|Put|Delete|Patch)\s*\(\s*\n\s*\*\s*path="([^"]+)"/gi;
  let methodMatch;

  while ((methodMatch = methodPattern.exec(content)) !== null) {
    const method = methodMatch[1].toUpperCase();
    const path = methodMatch[2];

    // Summary
    const summaryMatch = content.slice(methodMatch.index).match(/summary="([^"]+)"/);
    const summary = summaryMatch?.[1] ?? '';

    // Request body @OA\Property field'larını çıkar
    const requestFields = parseRequestFields(content, methodMatch.index);
    const responseFields = parseResponseFields(content, methodMatch.index);

    // PARAM_RULE constant'larını çıkar
    const paramRules = parseParamRules(content);

    endpoints.push({ plugin, method, path, summary, requestFields, responseFields, paramRules });
  }

  return endpoints;
}

function parseRequestFields(content: string, startIndex: number): ParsedField[] {
  const fields: ParsedField[] = [];
  const block = content.slice(startIndex, startIndex + 3000);

  // @OA\RequestBody bloğunu bul
  const reqBodyMatch = block.match(/@OA\\RequestBody[\s\S]*?@OA\\Response/);
  if (!reqBodyMatch) return fields;

  const reqBody = reqBodyMatch[0];

  // Her @OA\Property'yi parse et
  const propPattern = /@OA\\Property\s*\(([^)]+)\)/g;
  let propMatch;

  while ((propMatch = propPattern.exec(reqBody)) !== null) {
    const propContent = propMatch[1];

    const nameMatch = propContent.match(/property="([^"]+)"/);
    const typeMatch = propContent.match(/type="([^"]+)"/);
    const maxLenMatch = propContent.match(/maxLength=[\w\\:]+::(\w+)/);

    if (!nameMatch) continue;

    const name = nameMatch[1];
    const type = typeMatch?.[1] ?? 'string';

    // required array'inden kontrol et
    const requiredMatch = reqBody.match(/required=\{([^}]+)\}/);
    const required = requiredMatch ? requiredMatch[1].includes(`"${name}"`) : false;

    fields.push({ name, type, required, maxLength: undefined, notes: '' });
  }

  return fields;
}

function parseResponseFields(content: string, startIndex: number): ParsedField[] {
  // Model dosyasından response field'larını çıkar — ayrı parse
  return [];
}

function parseParamRules(content: string): string[] {
  const rules: string[] = [];
  const rulePattern = /PARAM_RULE_(\w+)\s*=\s*(\d+)/g;
  let match;

  while ((match = rulePattern.exec(content)) !== null) {
    rules.push(`${match[1]}: max ${match[2]} chars`);
  }

  return rules;
}

