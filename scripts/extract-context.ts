// scripts/extract-context.ts

import mysql from 'mysql2/promise';
import fs from 'fs';
import path from 'path';
import { config } from '../src/config/env';

const OUTPUT_DIR = path.resolve('src/context');

// ─────────────────────────────────────────────────────────────────────────────
// HELPERS
// ─────────────────────────────────────────────────────────────────────────────

function ensureOutputDir(): void {
  fs.mkdirSync(OUTPUT_DIR, { recursive: true });
}

function writeContext(filename: string, content: string): void {
  const filePath = path.join(OUTPUT_DIR, filename);
  fs.writeFileSync(filePath, content, 'utf-8');
  console.info(`✅ Written: ${filePath}`);
}

function header(title: string): string {
  const now = new Date().toISOString();
  return `# ${title}\n\n> Auto-generated by \`scripts/extract-context.ts\`\n> Last updated: ${now}\n> **Do not edit manually — run \`npm run context:extract\` to regenerate.**\n\n`;
}

